// há maior flexibilidade em atribuir variáveis
// porém em alguns casos isso pode gerar confusão

// em JS você pode identificar o tipo primitivo pelo typeof + variável
let qualquer = "Legal"
console.log(typeof qualquer)

qualquer = 123;

console.log(typeof qualquer)

// a gracinha acontece aqui. A variável "qualquer" deixou de ser string e virou um number
// por isso é importante você definir bem o nome da variável para não se confundir.



